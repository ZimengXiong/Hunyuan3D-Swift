import XCTest
import MLX
import Hy3DMLX

/// Threshold-gated shape parity vs the known-good Python MLX port. Thresholds are DESIGN.md §7.
/// Every test XCTSkips when its fixture (and, where needed, its checkpoint) is absent, so the
/// suite is green with zero fixtures present. Fixture names are documented in parity/README.md.
final class ShapeParityTests: XCTestCase {
    let fx = FixtureStore()

    /// Build DiT / VAE / DINOv2 from a checkpoint at fp32 (the fixtures are fp32).
    private func models(_ url: URL) throws -> (DiT, VAE, DINOv2) {
        let g = try ShapeGenerator(weightsURL: url, dtype: .float32)
        return (g.dit, g.vae, g.dino)
    }

    // §7: Shape DiT forward (mini) — cos ≥ 0.99999 (measured 1.0000000)
    func testDiTForwardMini() throws {
        let f = try fx.require("shape_dit_fixture.safetensors")
        let (dit, _, _) = try models(try fx.shapeWeights("small"))
        let cos = Metric.cosine(dit(f["x"]!, f["t"]!, f["cond"]!), f["v"]!)
        XCTAssertGreaterThanOrEqual(cos, 0.99999, "DiT (mini) cosine \(cos)")
    }

    // §7: Shape DiT forward (2.0-turbo) — cos ≥ 0.99999. Guidance-embed variant; fixture TBD.
    func testDiTForwardTurbo() throws {
        let f = try fx.require("shape_dit_fixture_turbo.safetensors")
        let (dit, _, _) = try models(try fx.shapeWeights("large"))
        let out = dit(f["x"]!, f["t"]!, f["cond"]!, guidance: f["guidance"])
        let cos = Metric.cosine(out, f["v"]!)
        XCTAssertGreaterThanOrEqual(cos, 0.99999, "DiT (2.0-turbo) cosine \(cos)")
    }

    // §7: Shape DINOv2 — cos ≥ 0.9999 (measured 0.9999999)
    func testDINOv2() throws {
        let f = try fx.require("shape_dino_fixture.safetensors")
        let (_, _, dino) = try models(try fx.shapeWeights("small"))
        let cos = Metric.cosine(dino(f["pixels"]!), f["out"]!)
        XCTAssertGreaterThanOrEqual(cos, 0.9999, "DINOv2 cosine \(cos)")
    }

    // §7: Shape VAE / geo-decoder grid — cos ≥ 0.9999 (measured 0.9999999)
    func testVAEGeoGrid() throws {
        let f = try fx.require("shape_vae_fixture.safetensors")
        let (_, vae, _) = try models(try fx.shapeWeights("small"))
        let got = vae.geoDecoder(f["q"]!, vae.decode(f["lat"]!))
        let cos = Metric.cosine(got, f["sdf"]!)
        XCTAssertGreaterThanOrEqual(cos, 0.9999, "VAE geo-grid cosine \(cos)")
    }

    // §7: Sigmas (flow-match + consistency) — maxabs ≤ 1e-6 (exact)
    // shape_sigmas_fixture.safetensors: flowmatch = flow_match_sigmas(30)[0], consistency = consistency_sigmas(8)
    func testSigmas() throws {
        let f = try fx.require("shape_sigmas_fixture.safetensors")
        let fm = Metric.maxabs(Sampler.flowMatchSigmas(f["flowmatch"]!.dim(0)), f["flowmatch"]!)
        XCTAssertLessThanOrEqual(fm, 1e-6, "flow-match sigmas maxabs \(fm)")
        let cs = Metric.maxabs(Sampler.consistencySigmas(f["consistency"]!.dim(0)), f["consistency"]!)
        XCTAssertLessThanOrEqual(cs, 1e-6, "consistency sigmas maxabs \(cs)")
    }

    // §7: Shape e2e mesh (same cond+noise) — Chamfer ≤ 0.01 of bbox (measured 0.0071)
    // Runs the fixed-cond/noise Swift denoise → VAE decode → octree grid → marching cubes, then
    // compares to the Python reference mesh generated by parity/python_from_fixture.py.
    private func runShapeE2E(weights: URL, cond: MLXArray, noise: MLXArray, sigmas: MLXArray,
                             guidance: Float, guidanceEmbed: Bool, resolution: Int) throws -> Mesh {
        let (dit, vae, _) = try models(weights)
        let pipe = Pipeline(dit: dit, vae: vae)
        let lat = pipe.denoise(cond: cond, noise: noise, sigmas: sigmas,
                               guidance: guidance, guidanceEmbed: guidanceEmbed)
        eval(lat)
        let grid = pipe.gridSDFOctree(latents: lat, resolution: resolution)
        eval(grid)
        return MarchingCubes.extract(grid: grid, level: 0.0)
    }

    func testShapeE2EMeshMini() throws {
        let f = try fx.require("shape_run_fixture.safetensors")
        let ref = try fx.require("shape_mesh_python_mini.safetensors")   // V: [n,3] float32
        let refVerts = simd3Array(ref["V"]!)
        let mesh = try runShapeE2E(weights: try fx.shapeWeights("small"),
                                   cond: f["cond"]!, noise: f["noise"]!, sigmas: f["sigmas"]!,
                                   guidance: f["guidance"]?.item(Float.self) ?? 5.0,
                                   guidanceEmbed: false, resolution: 256)   // 2mini: CFG
        XCTAssertFalse(mesh.vertices.isEmpty, "empty Swift mesh")
        let cd = chamferNormalized(mesh.vertices, refVerts)
        XCTAssertLessThanOrEqual(cd, 0.01, "shape e2e (mini) Chamfer/bbox \(cd)")
    }

    func testShapeE2EMeshTurbo() throws {
        let f = try fx.require("shape_run_fixture_turbo.safetensors")
        let ref = try fx.require("shape_mesh_python_turbo.safetensors")  // V: [n,3] float32
        let refVerts = simd3Array(ref["V"]!)
        let mesh = try runShapeE2E(weights: try fx.shapeWeights("large"),
                                   cond: f["cond"]!, noise: f["noise"]!, sigmas: f["sigmas"]!,
                                   guidance: f["guidance"]?.item(Float.self) ?? 5.0,
                                   guidanceEmbed: true, resolution: 256)    // 2.0-turbo: guidance-embed
        XCTAssertFalse(mesh.vertices.isEmpty, "empty Swift mesh")
        let cd = chamferNormalized(mesh.vertices, refVerts)
        XCTAssertLessThanOrEqual(cd, 0.01, "shape e2e (2.0-turbo) Chamfer/bbox \(cd)")
    }
}
