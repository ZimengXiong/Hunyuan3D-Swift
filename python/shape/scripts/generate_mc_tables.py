"""Generate marching-cubes lookup tables from the reference (skimage lewiner) for all 256 cube
sign-configs, and emit them as a Swift constant. Baked into the binary => no runtime Python.
  PYTHONPATH=. uv run python scripts/generate_mc_tables.py > swift/Sources/Hy3DMLX/MCTables.swift
"""
import sys
import numpy as np
from skimage import measure


def cpos(c):
    return (c & 1, (c >> 1) & 1, (c >> 2) & 1)


corners = [cpos(c) for c in range(8)]
# 12 edges: corner pairs differing in exactly one axis
edges = []
for a in range(8):
    for b in range(a + 1, 8):
        pa, pb = corners[a], corners[b]
        if sum(1 for i in range(3) if pa[i] != pb[i]) == 1:
            edges.append((a, b))


def emid(e):
    a, b = edges[e]
    return tuple(round((corners[a][i] + corners[b][i]) / 2, 3) for i in range(3))


mid2edge = {emid(e): e for e in range(12)}

tri_table = []
for config in range(256):
    vol = np.ones((2, 2, 2), dtype=np.float32)
    for c in range(8):
        if config & (1 << c):
            x, y, z = corners[c]
            vol[x, y, z] = -1.0
    tris = []
    if config not in (0, 255):
        try:
            verts, faces, _, _ = measure.marching_cubes(vol, 0.0, method="lewiner")
            ve = [mid2edge.get(tuple(np.round(v, 3)), -1) for v in verts]
            for f in faces:
                e = [ve[i] for i in f]
                if -1 not in e:
                    tris += [e[2], e[1], e[0]]  # reverse winding to match the torch pipeline
        except (ValueError, RuntimeError):
            tris = []
    tri_table.append(tris)

out = sys.stdout
out.write("// Generated by scripts/generate_mc_tables.py from skimage lewiner. Do not edit.\n")
out.write("public enum MCTables {\n")
out.write("    // each edge -> the two cube-corner indices it connects (corner i = (i&1, (i>>1)&1, (i>>2)&1))\n")
out.write("    public static let edgeCorners: [(Int, Int)] = [\n        "
          + ", ".join(f"({a},{b})" for a, b in edges) + ",\n    ]\n")
out.write("    // config (8-bit corner mask, bit i set if value<level) -> flat edge-index triples\n")
out.write("    public static let triTable: [[Int]] = [\n")
for tris in tri_table:
    out.write("        [" + ",".join(map(str, tris)) + "],\n")
out.write("    ]\n}\n")
